package day03_sequences_Variables;

public class Print_VS_Println {

    public static void main(String[] args) {

        System.out.println("A");
        System.out.println("B"); //println appends new line after every print statement
        System.out.println("C");

        System.out.println("=======================");

        System.out.print("A");
        System.out.print("B"); //print does not append new line
        System.out.print("C");



    }

}
