package day18_ForLoop;

public class forLoop_Practice {

    public static void main(String[] args) {

        for (int i = 1; i <= 5; i += 1){

            System.out.println("Cybertek Batch 20");

        }
        System.out.println("Done");
        System.out.println("===============================");


        for (int i1 = 1; i1 <= 10; i1 +=1){

            System.out.print(i1 + " ");

        }

        System.out.println("=================================");

        for (int i2 = 100; i2 >= 0; i2 -= 1){

            System.out.print(i2 + " ");

        }

    }

}
